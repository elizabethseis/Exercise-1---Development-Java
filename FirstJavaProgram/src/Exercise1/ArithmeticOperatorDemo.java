package Exercise1;

public class ArithmeticOperatorDemo {
	 public static void main(String args[]) {
	      int num1 = 100;
	      int num2 = 15;

	      System.out.println("num1 + num2: " + (num1 + num2) );
	      System.out.println("num1 - num2: " + (num1 - num2) );
	      System.out.println("num1 * num2: " + (num1 * num2) ); 
	      System.out.println("num1 / num2: " + (num1 / num2) );
	      System.out.println("num1 % num2: " + (num1 % num2) );
	   }
}
